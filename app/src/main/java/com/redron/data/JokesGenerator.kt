            "Черные"
        )
    )

    val jokes = mutableListOf<Joke>()

    fun generate() {
        jokes.clear()
        jokes.addAll(jokesList.shuffled())
    }

    fun getAllJokes(): List<Joke> {
        return jokes
    }

    fun getJoke(id: String): Joke? {
        return jokes.find { it.id == id }
    }

    fun addJoke(joke: Joke) {
        jokes.add(joke)
    }
}